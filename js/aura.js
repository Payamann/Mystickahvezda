        function calculateAura() {

            const dateInput = document.getElementById('birth-date').value;

            if (!dateInput) {

                document.getElementById('birth-date').style.borderColor = 'rgba(212,175,55,0.8)';

                return;

            }



            const digits = dateInput.replace(/-/g, '').split('').map(Number);

            let sum = digits.reduce((a, b) => a + b, 0);

            while (sum > 9) {

                sum = String(sum).split('').map(Number).reduce((a, b) => a + b, 0);

            }

            if (sum === 0) sum = 9;



            const aura = auras[sum];

            if (!aura) return;



            // Show orb

            const orb = document.getElementById('aura-orb');

            orb.style.background = `radial-gradient(circle at 35% 35%, ${aura.color}dd, ${aura.color}66)`;

            orb.style.boxShadow = `0 0 60px ${aura.glow}, 0 0 120px ${aura.glow}`;

            orb.style.cssText += `; before-background: ${aura.glow}`;

            orb.innerHTML = `<div style="position:absolute;inset:-20px;border-radius:50%;background:radial-gradient(circle,${aura.glow},transparent);animation:pulse 3s ease-in-out infinite;"></div>`;

            orb.classList.add('show');



            // Fill result

            const title = document.getElementById('aura-title');

            title.textContent = aura.name;

            title.style.color = aura.color;

            document.getElementById('aura-keyword').textContent = aura.keyword;

            document.getElementById('aura-desc').textContent = aura.desc;

            document.getElementById('aura-strengths').textContent = aura.strengths;

            document.getElementById('aura-challenges').textContent = aura.challenges;

            document.getElementById('aura-crystal').textContent = aura.crystal;

            document.getElementById('aura-chakra').textContent = aura.chakra;

            document.getElementById('aura-career').textContent = aura.career;



            const result = document.getElementById('aura-result');

            result.classList.add('show');

            orb.scrollIntoView({ behavior: 'smooth', block: 'center' });

        }



        document.getElementById('btn-aura-calc').addEventListener('click', calculateAura);

        document.getElementById('btn-aura-reset').addEventListener('click', () => {

            document.getElementById('birth-date').value = '';

            document.getElementById('aura-orb').className = 'aura-orb';

            document.getElementById('aura-result').className = 'aura-result';

        });

        document.getElementById('birth-date').addEventListener('keydown', e => {

            if (e.key === 'Enter') calculateAura();

        });
