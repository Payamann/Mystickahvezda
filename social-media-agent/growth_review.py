def parse_number(value: object) -> float:
    if value is None:
        return 0.0
    if isinstance(value, (int, float)):
        return float(value)
    if isinstance(value, (list, tuple)):
        values = [parse_number(item) for item in value]
        values = [item for item in values if item > 0]
        return sum(values) / len(values) if values else 0.0

    text = str(value).strip().replace("%", "")
    if not text:
        return 0.0

    text = re.sub(r"[^\d,.\-]", "", text)
    if not text or text in {"-", ".", ","}:
        return 0.0

    if "," in text and "." in text:
        # Keep the right-most separator as decimal, remove the other as thousands.
        if text.rfind(",") > text.rfind("."):
            text = text.replace(".", "").replace(",", ".")
        else:
            text = text.replace(",", "")
    elif "," in text:
        whole, _, fraction = text.rpartition(",")
        if len(fraction) == 3 and whole.replace("-", "").isdigit():
            text = whole + fraction
        else:
            text = text.replace(",", ".")

    try:
        return float(text)
    except ValueError:
        return 0.0


def parse_int(value: object) -> int:
    return int(round(parse_number(value)))


def normalize_header(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "_", value.strip().lower()).strip("_")


def normalized_row(row: dict[str, str]) -> dict[str, str]:
    return {normalize_header(key): value for key, value in row.items()}


def resolve_input_path(path: Path | str | None, base_dir: Path = BASE_DIR) -> Path | None:
    if path is None:
        return None

    candidate = Path(path)
    if candidate.is_absolute():
        return candidate

    cwd_candidate = Path.cwd() / candidate
    if cwd_candidate.exists():
        return cwd_candidate

    return base_dir / candidate


def load_csv_headers(path: Path | None) -> list[str]:
    if not path or not path.exists():
        return []
    with path.open(newline="", encoding="utf-8-sig") as f:
        reader = csv.DictReader(f)
        return [normalize_header(header) for header in reader.fieldnames or []]


def load_csv_rows(path: Path) -> list[dict[str, str]]:
    if not path or not path.exists():
        return []
    with path.open(newline="", encoding="utf-8-sig") as f:
        return [normalized_row(row) for row in csv.DictReader(f)]


