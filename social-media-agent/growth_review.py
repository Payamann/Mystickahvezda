def infer_feature_from_path(path: str) -> tuple[str, str]:
    clean_path = path.lower()
    for prefix, feature, target_type in FEATURE_BY_PATH:
        if clean_path == prefix or clean_path.startswith(prefix):
            return feature, target_type
    return "", "unknown"


def query_value(params: dict[str, list[str]], *names: str) -> str:
    for name in names:
        values = params.get(name)
        if values:
            return values[0].strip()
    return ""


def parse_pin_link(link: str) -> dict:
    parsed = urlparse(link)
    params = parse_qs(parsed.query)
    inferred_feature, target_type = infer_feature_from_path(parsed.path)
    source = query_value(params, "source", "entry_source")
    feature = query_value(params, "feature", "entry_feature")
    utms = {name: query_value(params, name) for name in REQUIRED_UTM_PARAMS}

    return {
        "path": parsed.path or link,
        "utm_source": utms["utm_source"],
        "utm_medium": utms["utm_medium"],
        "utm_campaign": utms["utm_campaign"],
        "utm_content": utms["utm_content"],
        "source": source,
        "feature": feature,
        "inferred_feature": inferred_feature,
        "target_type": target_type,
        "has_required_utms": all(utms.values()),
        "has_source_feature": bool(source and feature),
    }


def parse_date(value: str) -> date | None:
    if not value:
        return None
    text = value.strip()
    for fmt in ("%Y-%m-%d", "%d.%m.%Y", "%Y/%m/%d", "%d/%m/%Y"):
        try:
            return datetime.strptime(text[:10], fmt).date()
        except ValueError:
            pass
    try:
        return datetime.fromisoformat(text.replace("Z", "+00:00")).date()
    except ValueError:
        return None


def load_pinterest_inventory(path: Path | None, today: date | None = None) -> list[PinterestItem]:
    if not path:
        return []
    today = today or date.today()
    items = []
    for row in load_csv_rows(path):
        link = (row.get("link") or row.get("url") or row.get("destination") or "").strip()
        scheduled = (row.get("scheduled") or row.get("schedule") or "").strip()
        parsed_schedule = parse_date(scheduled)
        link_data = parse_pin_link(link)
        items.append(
            PinterestItem(
                title=(row.get("title") or row.get("pin_title") or "").strip(),
                link=link,
                board=(row.get("board") or "").strip(),
                scheduled=scheduled,
                is_stale=bool(parsed_schedule and parsed_schedule < today),
                **link_data,
            )
        )
    return items


def load_content_memory_summary(path: Path | None, days: int = 14, today: date | None = None) -> ContentMemorySummary:
    if not path or not path.exists():
        return ContentMemorySummary(0, 0, 0.0, {}, {}, list(TARGET_PILLARS), [], 0)

    today = today or date.today()
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return ContentMemorySummary(0, 0, 0.0, {}, {}, list(TARGET_PILLARS), [], 0)

    approved = data.get("approved_posts") or []
    recent = []
    for post in approved:
        post_date = parse_date(str(post.get("date") or ""))
        if post_date is None:
            continue
        if 0 <= (today - post_date).days <= days:
            recent.append(post)

    if not recent:
        recent = approved[-min(len(approved), 15):]

    post_type_counts = Counter(str(post.get("type") or post.get("post_type") or "unknown") for post in recent)
    pillar_counts = Counter()
    for post_type, count in post_type_counts.items():
        pillar_counts[PILLAR_BY_TYPE.get(post_type, "education")] += count

    scores = [
        parse_number(post.get("quality_score") or post.get("score") or post.get("qg_score"))
        for post in recent
    ]
    scores = [score for score in scores if score > 0]

    hooks = []
    hook_data = data.get("hook_performance") or data.get("hook_scores") or {}
    for hook, stats in hook_data.items():
        if isinstance(stats, dict):
            score = parse_number(stats.get("avg_score") or stats.get("score") or stats.get("quality_score"))
            count = parse_int(stats.get("count") or stats.get("uses") or stats.get("total"))
            raw_scores = stats.get("scores") or stats.get("values")
            if not score and isinstance(raw_scores, list):
                score = parse_number(raw_scores)
                count = len([item for item in raw_scores if parse_number(item) > 0])
        elif isinstance(stats, list):
            values = [parse_number(item) for item in stats]
            values = [value for value in values if value > 0]
            score = sum(values) / len(values) if values else 0.0
            count = len(values)
        else:
            score = parse_number(stats)
            count = 0
        hooks.append({"hook": hook, "avg_score": round(score, 1), "count": count})
    hooks.sort(key=lambda row: (row["avg_score"], row["count"]), reverse=True)

    return ContentMemorySummary(
        total_approved=len(approved),
        recent_count=len(recent),
        avg_quality=round(sum(scores) / len(scores), 1) if scores else 0.0,
        pillar_counts=dict(pillar_counts),
        post_type_counts=dict(post_type_counts),
        missing_pillars=[pillar for pillar in TARGET_PILLARS if pillar_counts.get(pillar, 0) == 0],
        top_hooks=hooks[:5],
        engagement_entries=len(data.get("engagement_log") or []),
    )


def summarize_pinterest(items: list[PinterestItem], segments: list[FunnelSegment]) -> dict:
    segment_by_pair = {(segment.source, segment.feature): segment for segment in segments}
    segments_by_feature: dict[str, list[FunnelSegment]] = defaultdict(list)
    for segment in segments:
        if segment.feature:
            segments_by_feature[segment.feature].append(segment)

    campaigns = {}
    for campaign_key, campaign_items_iter in group_items(items, key=lambda item: item.campaign_key).items():
        campaign_items = list(campaign_items_iter)
        feature_counts = Counter(item.matched_feature or "(unknown)" for item in campaign_items)
        feature = feature_counts.most_common(1)[0][0] if feature_counts else "(unknown)"
        direct_matches = [
            segment_by_pair[(item.source, item.feature)]
            for item in campaign_items
            if item.has_source_feature and (item.source, item.feature) in segment_by_pair
        ]
        feature_matches = segments_by_feature.get(feature, []) if feature != "(unknown)" else []
        best_segment = None
        if direct_matches:
            best_segment = sorted(direct_matches, key=lambda segment: segment.score, reverse=True)[0]
        elif feature_matches:
            best_segment = sorted(feature_matches, key=lambda segment: segment.score, reverse=True)[0]

        campaigns[campaign_key] = {
            "pins": len(campaign_items),
            "stale_pins": sum(1 for item in campaign_items if item.is_stale),
            "missing_utm_pins": sum(1 for item in campaign_items if not item.has_required_utms),
            "source_feature_ready_pins": sum(1 for item in campaign_items if item.has_source_feature),
            "ambiguous_pins": sum(1 for item in campaign_items if item.has_required_utms and not item.has_source_feature),
            "feature": feature,
            "target_type": Counter(item.target_type for item in campaign_items).most_common(1)[0][0],
            "best_segment": best_segment.to_dict() if best_segment else None,
            "match_type": "source_feature" if direct_matches else ("feature" if best_segment else "none"),
        }

    ranked_campaigns = sorted(
        campaigns.items(),
        key=lambda row: (
            row[1]["best_segment"]["score"] if row[1]["best_segment"] else 0,
            row[1]["pins"],
        ),
        reverse=True,
    )

    return {
        "total_pins": len(items),
        "stale_pins": sum(1 for item in items if item.is_stale),
        "missing_utm_pins": sum(1 for item in items if not item.has_required_utms),
        "source_feature_ready_pins": sum(1 for item in items if item.has_source_feature),
        "ambiguous_pins": sum(1 for item in items if item.has_required_utms and not item.has_source_feature),
        "campaigns": dict(ranked_campaigns),
        "top_campaigns": [{"campaign": key, **value} for key, value in ranked_campaigns[:10]],
    }


def group_items(items: list[PinterestItem], key) -> dict[str, list[PinterestItem]]:
    grouped: dict[str, list[PinterestItem]] = defaultdict(list)
    for item in items:
        grouped[key(item)].append(item)
    return grouped


def summarize_funnel(segments: list[FunnelSegment]) -> dict:
    top_segments = sorted(segments, key=lambda segment: segment.score, reverse=True)[:10]
    return {
        "segments": len(segments),
        "total_events": sum(segment.total_events for segment in segments),
        "paywall_viewed": sum(segment.paywall_viewed for segment in segments),
        "checkout_requested": sum(segment.checkout_requested for segment in segments),
        "checkout_started": sum(segment.checkout_started for segment in segments),
        "purchase_completed": sum(segment.purchase_completed for segment in segments),
        "failures": sum(segment.failures for segment in segments),
        "top_segments": [segment.to_dict() for segment in top_segments],
    }


